package com.jspgou.cms.entity;

import java.math.BigDecimal;

/**
 * Created by zj on 15-7-31.
 */
public class BaseProductReport {

    private String productName;
    private BigDecimal price;
    private Integer sell;
    private Integer refund;
    private Integer order;
    private Integer used;
    private BigDecimal usedAmount;
    private BigDecimal receivedAmount;
    private BigDecimal refundAmount;
    private BigDecimal realReceivedAmount;

    public BaseProductReport(){

    }

    public BaseProductReport(String productName, BigDecimal price, Integer sell, Integer refund,
                             Integer order, Integer used, BigDecimal usedAmount, BigDecimal receivedAmount,
                             BigDecimal refundAmount, BigDecimal realReceivedAmount) {
        this.productName = productName;
        this.price = price;
        this.sell = sell;
        this.refund = refund;
        this.order = order;
        this.used = used;
        this.usedAmount = usedAmount;
        this.receivedAmount = receivedAmount;
        this.refundAmount = refundAmount;
        this.realReceivedAmount = realReceivedAmount;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public Integer getSell() {
        return sell;
    }

    public void setSell(Integer sell) {
        this.sell = sell;
    }

    public Integer getRefund() {
        return refund;
    }

    public void setRefund(Integer refund) {
        this.refund = refund;
    }

    public Integer getOrder() {
        return order;
    }

    public void setOrder(Integer order) {
        this.order = order;
    }

    public Integer getUsed() {
        return used;
    }

    public void setUsed(Integer used) {
        this.used = used;
    }

    public BigDecimal getUsedAmount() {
        return usedAmount;
    }

    public void setUsedAmount(BigDecimal usedAmount) {
        this.usedAmount = usedAmount;
    }

    public BigDecimal getReceivedAmount() {
        return receivedAmount;
    }

    public void setReceivedAmount(BigDecimal receivedAmount) {
        this.receivedAmount = receivedAmount;
    }

    public BigDecimal getRefundAmount() {
        return refundAmount;
    }

    public void setRefundAmount(BigDecimal refundAmount) {
        this.refundAmount = refundAmount;
    }

    public BigDecimal getRealReceivedAmount() {
        return realReceivedAmount;
    }

    public void setRealReceivedAmount(BigDecimal realReceivedAmount) {
        this.realReceivedAmount = realReceivedAmount;
    }
}
